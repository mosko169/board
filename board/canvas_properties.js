

class CanvasProperties {
    constructor(height, width, fillStyle, strokeStyle, lineWidth, lineCap) {
        this.height = height;
        this.width = width;
        this.fillStyle = fillStyle;
        this.strokeStyle = strokeStyle;
        this.lineWidth = lineWidth;
        this.lineCap = lineCap;
    }
}

module.exports = CanvasProperties;
